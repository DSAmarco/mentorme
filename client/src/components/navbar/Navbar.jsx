import React, { useEffect, useState } from "react";
import { Link, useLocation, useNavigate } from "react-router-dom";
import newRequest from "../../utils/newRequest";
import "./Navbar.scss";

function Navbar() {
  const [active, setActive] = useState(false);
  const [open, setOpen] = useState(false);

  const { pathname } = useLocation();

  const isActive = () => {
    window.scrollY > 0 ? setActive(true) : setActive(false);
  };

  useEffect(() => {
    window.addEventListener("scroll", isActive);
    return () => {
      window.removeEventListener("scroll", isActive);
    };
  }, []);

  const currentUser = JSON.parse(localStorage.getItem("currentUser"));

  const navigate = useNavigate();

  const handleLogout = async () => {
    try {
      await newRequest.post("/auth/logout");
      localStorage.setItem("currentUser", null);
      navigate("/");
    } catch (err) {
      console.log(err);
    }
  };

  const handleLinkClick = () => {
    setTimeout(() => {
      window.location.reload(); // Reload the window after a delay
    }, 1); // Delay in milliseconds (e.g., 1000 milliseconds = 1 second)
  };
  return (
    <div className={active || pathname !== "/" ? "navbar active" : "navbar"}>
      <div className="container">
        <div className="logo">
          <Link className="link" to="/">
            <span className="text">MentorMe</span>
          </Link>
        </div>
        <div className="links">
          {!currentUser?.isSeller && <span></span>}
          {currentUser ? (
            <div className="user" onClick={() => setOpen(!open)}>
              <img src={currentUser.img || "/img/noavatar.jpg"} alt="" />
              <span>{currentUser?.username}</span>
              {open && (
                <div className="options">
                  {currentUser.isSeller && (
                    <>
                      <Link className="link" to="/mygigs">
                        Mentorships
                      </Link>
                      <Link className="link" to="/add">
                        Add New Mentorship
                      </Link>
                    </>
                  )}
                  <Link className="link" to="/orders">
                    Orders
                  </Link>
                  <Link className="link" to="/messages">
                    Messages
                  </Link>
                  <Link className="link" to="/edit">
                    Edit Account
                  </Link>
                  <Link className="link" onClick={handleLogout}>
                    Logout
                  </Link>
                </div>
              )}
            </div>
          ) : (
            <>
              <Link to="/login" className="link">Sign in</Link>
              <Link className="link" to="/register">
                <button>Join</button>
              </Link>
            </>
          )}
        </div>
      </div>
      {(active || pathname !== "/") && (
        <>
          <hr />
          <div className="menu">
            <Link className="link menuLink" to="/gigs?cat=city" onClick={handleLinkClick}>
              City
            </Link>
            <Link className="link menuLink" to="/gigs?cat=culture" onClick={handleLinkClick}>
              Culture
            </Link>
            <Link className="link menuLink" to="/gigs?cat=food" onClick={handleLinkClick}>
              Food
            </Link>
            <Link className="link menuLink" to="/gigs?cat=activity" onClick={handleLinkClick}>
              Activity
            </Link>

          </div>
          <hr />
        </>
      )}
    </div>
  );
}

export default Navbar;
